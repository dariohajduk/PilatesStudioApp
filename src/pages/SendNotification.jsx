const SendNotification = () => {
  const [phone, setPhone] = useState("");
  const [message, setMessage] = useState("");
  const [loading, setLoading] = useState(false);

  // שליחת ההתראה
  const handleSendNotification = async () => {
    if (!phone || !message) {
      toast.error("אנא מלא את כל השדות");
      return;
    }
  
    setLoading(true);
  
    try {
      // שליפת ה־FCM Token מה־Firestore לפי מספר הטלפון
      const userRef = doc(db, "Users", phone);
      const userSnap = await getDoc(userRef);
  
      if (!userSnap.exists()) {
        toast.error("משתמש לא נמצא");
        setLoading(false);
        return;
      }
  
      const userData = userSnap.data();
      const token = userData.fcmToken;
  
      const response = await fetch("/api/sendNotification", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ token, message }),
      });
  
      const result = await response.json();
  
      if (result.success) {
        toast.success("הודעה נשלחה בהצלחה!");
      } else {
        toast.error("שליחה נכשלה: " + result.error);
      }
    } catch (err) {
      console.error("שגיאה בשליחת התראה:", err);
      toast.error("שגיאה כללית");
    }
  
    setLoading(false);
  };
  
  

  return (
    <div className="p-4">
      <h1 className="text-xl font-bold mb-4">שלח התראה למשתמש</h1>

      <div className="mb-4">
        <label className="block mb-2">מספר טלפון:</label>
        <input
          type="tel"
          className="w-full p-2 border rounded"
          value={phone}
          onChange={(e) => setPhone(e.target.value)}
          placeholder="הזן מספר טלפון"
        />
      </div>

      <div className="mb-4">
        <label className="block mb-2">הודעת התראה:</label>
        <textarea
          className="w-full p-2 border rounded"
          value={message}
          onChange={(e) => setMessage(e.target.value)}
          placeholder="הזן את ההודעה לשליחה"
        />
      </div>

      <button
        onClick={handleSendNotification}
        className={`w-full p-2 bg-blue-600 text-white rounded ${loading ? "cursor-not-allowed" : ""}`}
        disabled={loading}
      >
        {loading ? "שולח..." : "שלח התראה"}
      </button>
    </div>
  );
};

export default SendNotification;
